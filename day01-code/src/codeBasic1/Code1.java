package codeBasic1;

public class Code1 {
    public static void main(String[] args){
        //1.& йҖ»иҫ‘дёҺ
        // иҝҗз®—и§„еҲҷ: 0дёәfalse, 1дёәtrue
        int a = 200 ; //0000 0000 0000 0000 0000 0000 1100 1000
        int b = 10 ;  //0000 0000 0000 0000 0000 0000 0000 1010
        //еҺҹз ҒиҝӣиЎҢ & йҖ»иҫ‘дё” иҝҗз®—,0дёәfalse,1дёәtrue
        //з»“жһңдёә: 0000 0000 0000 0000 0000 0000 0000 1000
        System.out.println(a & b); //8

        //2. | йҖ»иҫ‘жҲ–
        // иҝҗз®—и§„еҲҷ: 0дёәfalse, 1дёәtrue
        int c = 200; //0000 0000 0000 0000 0000 0000 1100 1000
        int d = 10;  //0000 0000 0000 0000 0000 0000 0000 1010
        //еҺҹз ҒиҝӣиЎҢ|йҖ»иҫ‘жҲ– иҝҗз®—
        //з»“жһңдёә: 0000 0000 0000 0000 0000 0000 1100 1010
        System.out.println(c | d); //202

        //3. << е·Ұз§»
        //иҝҗз®—и§„еҲҷ: еҗ‘е·Ұз§»еҠЁ,дҪҺдҪҚиЎҘ0
        int e = 200;//0000 0000 0000 0000 0000 0000 1100 1000
        //еҗ‘е·Ұз§»еҠЁдёӨж јеҗҺ 0000 0000 0000 0000 0000 0011 0010 0000
        System.out.println(e << 2); // 800

        //4.>> еҸіз§»
        // иҝҗз®—и§„еҲҷ: еҗ‘еҸіз§»еҠЁ,й«ҳдҪҚиЎҘ0жҲ–1
        int f = 200; //0000 0000 0000 0000 0000 0000 1100 1000
        //0000 0000 0000 0000 0000 0000 0011 0010
        System.out.println(f >> 2); //50
    }
}
